void Map::Draw() {

	//ごめん、うちのPCだと見ずらいので一時的に白い背景おく。後で消していいよ
	DrawBox(0, 0, 1280, 720, GetColor(200, 200, 200), true);
	//-------

	for (int y = 0; y < MAP_DATA_Y; y++)
	{
		for (int x = 0; x < MAP_DATA_X; x++)
		{
			// ブロックを描画
			if (m_MapData[y][x] == 1) {
				DrawGraph(x * MAP_SIZE, y * MAP_SIZE, mapHundle1, true);
			}
		}
	}
}

